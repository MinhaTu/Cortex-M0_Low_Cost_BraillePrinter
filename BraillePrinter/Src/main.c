/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM14_Init();
  MX_TIM3_Init();
  MX_TIM1_Init();
  /* USER CODE BEGIN 2 */

  	initMotors();

	//memset(linhaBraille, 0, sizeof(linhaBraille));
	memset(buffer_braille, 0, sizeof(buffer_braille));

	keyboardBegin(&keyboard, PS2_DATA_PORT, PS2_DATA_PIN, PS2_IQR_PORT, PS2_IQR_PIN);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
	/* Faz a leitura do teclado e envia ao buffer_char */
	if(keyboardAvailable(&keyboard)){
		uint8_t c = keyboardRead(&keyboard);
		if(c == PS2_ENTER){
			pressedEnter = 1;
			break;
		}
		feedBuffer(buffer_char, MAX_CARACTERES, c);
	}
	/* Programa leitura do teclado */
	if(pressedEnter){
		for(int j=0;j<3;j++){
		//Linhas das matrizes
			for(int i=0;i<MAX_CARACTERES;i++){
				//Recebe os pontos da linha para o caractere atual
				fillLineWithBraille(buffer_braille, j,buffer_char[i]);

				//Percorre os 4 bits no mÃ¡ximo para cada caractere
				for(int x = 0; x < 4; ++x){
					if(buffer_braille[x] == '\0'){
						break;
					}else if(buffer_braille[x] == 1){
						//Furar
					}
					setPoint_1 += DELTA_COL_LIN;
					while(isDone == 0){
						atualizarEixoX();
					}
					isDone = 0;
				}

				setPoint_1 += DELTA_CHAR_H;
				while(isDone == 0){
					atualizarEixoX();
				}
				isDone = 0;
			}

			setPoint_2 += DELTA_CHAR_V;
			while(isDone == 0){
				atualizarEixoY();
			}
			isDone = 0;

			pressedEnter = 0;
		}
	}
	clearBuffer(buffer_char);

  }
  /* USER CODE END 3 */

}

